package com.hang.cherry.model;

/**
 * XItem
 *
 * @author Hang W
 */
public class XItem {

    private double high;

    private double time;

    private String name;

    public double getHigh() {
        return high;
    }

    public void setHigh(double high) {
        this.high = high;
    }

    public double getTime() {
        return time;
    }

    public void setTime(double time) {
        this.time = time;
    }

    public String getName() {
        return name;
    }

    public void setName(String name) {
        this.name = name;
    }

}
